import * as ActionTypes from './ActionTypes';
import { DISHES } from '../shared/dishes';
import {baseUrl} from '../shared/baseUrl';

export const addComment = (dishId, rating, author, comment) => ({
    type: ActionTypes.ADD_COMMENT,
    payload: {
        dishId: dishId,
        rating: rating,
        author: author,
        comment: comment
    }
});

export const fetchDishes = () => (dispatch) => {    //this is thunk 

    dispatch(dishesLoading(true));

    return fetch(baseUrl + 'dishes')
        .then(response => {
            if (response.ok) {
                return response;
            }
            else {
                var error = new Error('Error' + response.status + ':' + response.statusText)  //creting error object in java script specifying the error
                error.response = response;
                throw error;
            }
        },
        error => {       //dont hear anything back from the server, so thats where u have to handle the error.
            var errmess = new Error(error.message);
            throw errmess;
        })
        .then(response => response.json())  //convert incoming response to json. it is a call back function
        .then(dishes => dispatch(addDishes(dishes)))
        .catch(error => dispatch(dishesFailed(error.message)));
}

export const dishesLoading = () => ({
    type: ActionTypes.DISHES_LOADING
});

export const dishesFailed = (errmess) => ({   //this is function which returns something.
    type: ActionTypes.DISHES_FAILED,  //returning action object
    payload: errmess
});

export const addDishes = (dishes) => ({
    type: ActionTypes.ADD_DISHES,
    payload: dishes
});

